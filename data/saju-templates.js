/**
 * 사주 계산 결과 + 십신 분석 결과로 관련 템플릿을 모아 반환합니다.
 *
 * @param {object} pillars        — computeFourPillars() 결과
 * @param {object} tenGodAnalysis — analyzeTenGods() 결과
 * @returns {object} 조합된 템플릿 데이터
 */
export function getTemplatesForReading(pillars, tenGodAnalysis) {
  const dayStem = pillars.day.stem;

  return {
    // 일간 기본 성향
    dayStem: DAY_STEM_TEMPLATES[dayStem] || null,

    // 격국 해석
    structure: STRUCTURE_TEMPLATES[tenGodAnalysis.structure] || null,

    // 각 주의 십신과 해석
    positions: ['year', 'month', 'day', 'hour'].map(pos => {
      const tg = tenGodAnalysis.pillars[pos];
      return {
        position: POSITION_MEANINGS[pos],
        stem: {
          tenGod: tg.stem,
          template: tg.stem !== '일간' ? TEN_GOD_TEMPLATES[tg.stem] || null : null,
        },
        branch: {
          tenGod: tg.branch,
          template: TEN_GOD_TEMPLATES[tg.branch] || null,
        },
        hiddenStems: tg.hiddenStems,
      };
    }),

    // 십신 분포 요약
    summary: tenGodAnalysis.summary,
    dominantTenGod: tenGodAnalysis.dominant,
    dominantTenGodTemplate: TEN_GOD_TEMPLATES[tenGodAnalysis.dominant] || null,
  };
}
